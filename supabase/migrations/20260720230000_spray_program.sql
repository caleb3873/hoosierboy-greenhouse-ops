-- Location: null = all houses; 'west' | 'main' | 'main+quonsets' | 'sprague' | 'houseplants'.

create table if not exists spray_program (
  id uuid primary key default gen_random_uuid(),
  year int not null,
  week_number int not null,
  location text,
  product_name text not null,
  product_id uuid references chem_products(id),
  rate text,
  sort_order int not null default 0,
  notes text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create index if not exists spray_program_year_week_idx on spray_program (year, week_number);
alter table spray_program enable row level security;
drop policy if exists spray_program_all on spray_program;
create policy spray_program_all on spray_program for all using (true) with check (true);

